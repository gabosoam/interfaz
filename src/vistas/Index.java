    public void poblar() {
        refrescar();
        DefaultTableModel modelo = (DefaultTableModel) tablaPersonas.getModel();

        Persona persona = new Persona();

        List<Persona> listaPersonas = persona.poblar();

        for (int i = 0; i < listaPersonas.size(); i++) {
            modelo.addRow(new Object[]{
                listaPersonas.get(i).getId(),
                listaPersonas.get(i).getNombre(),
                listaPersonas.get(i).getDireccion()
            }
            );
        }
    }
    
    
